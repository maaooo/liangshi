//记录下windows下的宋体对应用pixel size, 这样，所有的系统都这样转化到pixel size，包括win
int FontUtil::GetStandardFontPixelSize(int size)
{
	static map<int, int> sizeMap;

	if(sizeMap.size() == 0)
	{
		InitSizeMap(sizeMap);
	}
	return sizeMap[size];
}

bool FontUtil::HasNoneEnglish(QString& str)
{
	for(int i = 0; i < str.length(); ++i)
	{
		QChar c = str.at(i);
		if(c.unicode() > 128)
			return true;
	}
	return false;
}

